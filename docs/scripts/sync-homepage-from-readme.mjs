import { cp, mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const docsRoot = path.resolve(scriptDir, '..')
const repoRoot = path.resolve(docsRoot, '..')

const readmePath = path.join(repoRoot, 'README.md')
const mediaSourceDir = path.join(repoRoot, 'media')
const mediaTargetDir = path.join(docsRoot, 'static', 'media')
const homepagePath = path.join(docsRoot, 'src', 'pages', 'index.mdx')

const homepageFrontMatter = `---
title: Laika
slug: /
hide_title: true
hide_table_of_contents: true
custom_edit_url: https://github.com/zendesk/laika/edit/main/README.md
---

import useBaseUrl from '@docusaurus/useBaseUrl'

<!-- This file is generated by docs/scripts/sync-homepage-from-readme.mjs. -->

`

const homepageContent = (await readFile(readmePath, 'utf8'))
  .replace(/\(https:\/\/zendesk\.github\.io\/laika\/([^)]+)\)/g, '(./$1)')
  .replace(
    /!\[([^\]]*)\]\(\.\/media\/([^)]+)\)/g,
    (_, altText, fileName) =>
      `<!-- prettier-ignore -->
<img src={useBaseUrl('/media/${fileName}')} alt="${altText}" />`,
  )

await mkdir(mediaTargetDir, { recursive: true })
await cp(mediaSourceDir, mediaTargetDir, {
  force: true,
  recursive: true,
})

await writeFile(homepagePath, `${homepageFrontMatter}${homepageContent}`)
