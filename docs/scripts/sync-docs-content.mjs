import { cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const docsRoot = path.resolve(scriptDir, '..')
const repoRoot = path.resolve(docsRoot, '..')

const readmePath = path.join(repoRoot, 'README.md')
const mediaSourceDir = path.join(repoRoot, 'media')
const mediaTargetDir = path.join(docsRoot, 'static', 'readme-media')
const readmeDocPath = path.join(docsRoot, 'docs', 'readme.mdx')

const frontMatter = `---
id: 'readme'
title: 'Laika'
slug: /
hide_title: true
hide_table_of_contents: true
custom_edit_url: https://github.com/zendesk/laika/edit/main/README.md
---

import useBaseUrl from '@docusaurus/useBaseUrl'

<!-- This file is generated by docs/scripts/sync-docs-content.mjs. -->

`

const rewriteDocsLink = (_, docsPath) => `(pathname:///${docsPath})`

const readmeContent = (await readFile(readmePath, 'utf8'))
  .replace(
    /\(https:\/\/zendesk\.github\.io\/laika\/(docs(?:\/[^)#]+)?(?:#[^)]+)?)\)/g,
    rewriteDocsLink,
  )
  .replace(/\(\.\/(docs(?:\/[^)#]+)?(?:#[^)]+)?)\)/g, rewriteDocsLink)
  .replace(/\(\/laika\/(docs(?:\/[^)#]+)?(?:#[^)]+)?)\)/g, rewriteDocsLink)
  .replace(
    /!\[([^\]]*)\]\(\.\/media\/([^)]+)\)/g,
    (_, altText, fileName) =>
      `<!-- prettier-ignore -->\n<img src={useBaseUrl('/readme-media/${fileName}')} alt=${JSON.stringify(
        altText,
      )} />`,
  )

await rm(mediaTargetDir, { force: true, recursive: true })
await mkdir(mediaTargetDir, { recursive: true })
await cp(mediaSourceDir, mediaTargetDir, { force: true, recursive: true })

await writeFile(readmeDocPath, `${frontMatter}${readmeContent}`)
